      <h2>7. 📦 Productos y publicaciones</h2>
      <h3>7.1 Contenido permitido</h3>
      <p>Solo se permite la publicación de productos alimenticios de panadería, pastelería y repostería artesanal. Queda
        prohibida la publicación de:</p>
      <ul>
        <li>Productos no alimenticios.</li>
        <li>Alimentos que requieran cadena de frío y no puedan garantizarla.</li>
        <li>Productos cuya comercialización esté prohibida por la legislación argentina.</li>
        <li>Contenido engañoso, falso o que induzca a error al consumidor.</li>
      </ul>
      <h3>7.2 Imágenes y descripciones</h3>
      <p>Las imágenes publicadas deben corresponder fielmente al producto ofrecido. El uso de imágenes de terceros sin
        autorización, que infrinjan derechos de propiedad intelectual, queda expresamente prohibido. PanaderiaMarket
        podrá eliminar sin previo aviso publicaciones que incumplan estas disposiciones.</p>
      <h3>7.3 Alérgenos</h3>
      <p>Conforme a la Resolución Conjunta N° 201/2011 del Ministerio de Salud y la Resolución ANMAT N° 834/2010, el
        vendedor debe declarar en la descripción del producto la presencia de los 14 alérgenos de declaración
        obligatoria: gluten, crustáceos, huevos, pescado, maníes, soja, leche, frutos de cáscara, apio, mostaza, sésamo,
        dióxido de azufre y sulfitos, altramuces y moluscos.</p>
    </div>

