      <h2>10. 🔄 Devoluciones y reclamos</h2>
      <h3>10.1 Derecho del consumidor</h3>
      <p>Conforme al artículo 10 bis y concordantes de la Ley N° 24.240 de Defensa del Consumidor (modificada por Ley N°
        26.361), el comprador tiene derecho a:</p>
      <ul>
        <li>Exigir el cumplimiento de la prestación en los términos ofertados.</li>
        <li>Aceptar otro producto o prestación de servicio equivalente.</li>
        <li>Rescindir el contrato con derecho a la restitución del precio pagado.</li>
      </ul>
      <h3>10.2 Productos en mal estado</h3>
      <p>Si el producto recibido no corresponde al publicado, presenta problemas de calidad o inocuidad, el comprador
        deberá notificarlo al vendedor dentro de las 24 horas de recibido el producto. Ante productos en mal estado que
        pudieran representar un riesgo para la salud, el comprador podrá también realizar la denuncia correspondiente
        ante el ANMAT o la autoridad sanitaria provincial.</p>
      <h3>10.3 Canal de reclamos</h3>
      <p>PanaderiaMarket dispone como canal de contacto el correo electrónico y el número de WhatsApp del operador. Para
        reclamos que no puedan resolverse entre las partes, el comprador podrá recurrir a:</p>
      <ul>
        <li>Dirección de Defensa del Consumidor de la Provincia de Catamarca.</li>
        <li>Sistema Nacional de Arbitraje de Consumo (SNAC).</li>
        <li>Portal www.consumidor.gob.ar del Ministerio de Economía de la Nación.</li>
      </ul>
    </div>

