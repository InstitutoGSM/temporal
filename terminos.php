      <h2>5. 🍞 Obligaciones del vendedor</h2>
      <h3>5.1 Habilitaciones y certificaciones</h3>
      <p>Todo vendedor que opere en PanaderiaMarket declara bajo juramento que cuenta con:</p>
      <ul>
        <li>Habilitación municipal del local o establecimiento de elaboración, conforme a la normativa de cada municipio
          de la Provincia de Catamarca.</li>
        <li>Certificado de manipulación de alimentos vigente, emitido por autoridad sanitaria competente, conforme a la
          Ley N° 18.284 (Código Alimentario Argentino) y sus normas reglamentarias.</li>
        <li>Cumplimiento de las Buenas Prácticas de Manufactura (BPM) establecidas en el Capítulo N° II del Código
          Alimentario Argentino.</li>
        <li>Registro ante el organismo competente (ANMAT, SENASA o autoridad provincial) cuando corresponda por el tipo
          de producto elaborado.</li>
        <li>Libreta sanitaria al día para todos los manipuladores de alimentos que intervengan en la elaboración.</li>
      </ul>
      <h3>5.2 Veracidad de la información</h3>
      <p>El vendedor se compromete a publicar información veraz, completa y actualizada sobre sus productos, incluyendo
        descripción, ingredientes relevantes, alérgenos, precio, disponibilidad y condiciones de entrega. Queda
        expresamente prohibido publicar productos que:</p>
      <ul>
        <li>No cumplan con los estándares del Código Alimentario Argentino (Ley N° 18.284).</li>
        <li>Contengan sustancias no declaradas, especialmente alérgenos contemplados en la Resolución ANMAT N° 834/2010
          y la Ley N° 26.588 (celiaquía).</li>
        <li>Hayan sido elaborados en condiciones insalubres.</li>
        <li>Estén vencidos o en mal estado.</li>
      </ul>
      <h3>5.3 Precios y disponibilidad</h3>
      <p>Los precios publicados deben incluir todos los impuestos aplicables conforme a la Ley N° 24.240 de Defensa del
        Consumidor y la Resolución 7/2002 de la Secretaría de Coordinación Técnica. El vendedor es responsable de
        mantener actualizada la disponibilidad de sus productos.</p>
      <h3>5.4 Entrega y cumplimiento</h3>
      <p>El vendedor se compromete a cumplir con los pedidos confirmados en tiempo y forma. En caso de imposibilidad de
        cumplimiento, deberá notificarlo al comprador con la mayor anticipación posible y gestionar la devolución del
        pago si correspondiere.</p>
