      <h2>8. 💵 Precios y medios de pago</h2>
      <h3>8.1 Precios</h3>
      <p>Todos los precios publicados en PanaderiaMarket están expresados en pesos argentinos (ARS) e incluyen el
        Impuesto al Valor Agregado (IVA) cuando corresponda. PanaderiaMarket no cobra comisiones al comprador sobre el
        precio de los productos.</p>
      <h3>8.2 Medios de pago</h3>
      <p>Los medios de pago disponibles dependen de lo configurado por cada vendedor y pueden incluir:</p>
      <ul>
        <li><strong>Efectivo:</strong> pago al momento de la entrega o retiro.</li>
        <li><strong>Transferencia bancaria:</strong> mediante CBU o alias proporcionado por el vendedor.</li>
        <li><strong>Tarjeta de débito o crédito:</strong> sujeto a disponibilidad del vendedor.</li>
      </ul>
      <h3>8.3 Seguridad de datos de pago</h3>
      <p>PanaderiaMarket no almacena números completos de tarjetas de crédito o débito. Solo se conservan los últimos 4
        dígitos con fines de identificación, conforme a las normas PCI-DSS y a la Ley N° 25.326 de Protección de Datos
        Personales. El CVV/CVC nunca es almacenado bajo ninguna circunstancia.</p>
      <h3>8.4 Variación de precios</h3>
      <p>Los vendedores pueden modificar sus precios en cualquier momento. El precio aplicable será el vigente al
        momento de la confirmación del pedido. PanaderiaMarket no se responsabiliza por variaciones de precios ocurridas
        entre la visualización del producto y la confirmación del pedido.</p>
