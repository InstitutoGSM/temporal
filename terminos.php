      <h2>9. 📬 Pedidos y entregas</h2>
      <h3>9.1 Proceso del pedido</h3>
      <p>Un pedido se considera confirmado cuando el comprador completa el proceso de checkout y recibe el ticket de
        confirmación con número único. A partir de ese momento, el vendedor recibe la notificación y debe confirmar la
        disponibilidad.</p>
      <h3>9.2 Plazos de entrega</h3>
      <p>Los plazos de entrega son acordados directamente entre comprador y vendedor. PanaderiaMarket no garantiza
        plazos específicos ni se responsabiliza por demoras. Se recomienda que el comprador coordine los tiempos con el
        vendedor a través de los canales de contacto disponibles (WhatsApp, teléfono, email).</p>
      <h3>9.3 Cancelación de pedidos</h3>
      <p>El comprador podrá cancelar un pedido siempre que el vendedor no haya iniciado su elaboración. Una vez iniciada
        la elaboración del producto, el vendedor no está obligado a aceptar la cancelación. Se recomienda comunicarse
        directamente con el vendedor a la brevedad ante cualquier necesidad de cancelación.</p>
      <h3>9.4 Ticket de pedido</h3>
      <p>Al confirmar cada pedido, la plataforma genera automáticamente un comprobante digital (ticket) con número único
        que sirve como constancia de la transacción. Este documento tiene validez como comprobante de compra conforme a
        la normativa de defensa del consumidor.</p>
    </div>

